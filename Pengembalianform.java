    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {

        String id = jComboBox1.getSelectedItem().toString();

        String sql = "SELECT * FROM transaksi WHERE id_transaksi=?";

        PreparedStatement pst =
                koneksi.koneksi.getKoneksi().prepareStatement(sql);

        pst.setString(1, id);

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {

            java.text.SimpleDateFormat sdf =
                    new java.text.SimpleDateFormat("yyyy-MM-dd");

            Date tanggalSewa =
                    rs.getDate("tanggal_sewa");

            int lama =
                    rs.getInt("lama_sewa");

            Calendar cal = Calendar.getInstance();

            cal.setTime(tanggalSewa);

            cal.add(Calendar.DATE, lama);

            Date jatuhTempo = cal.getTime();

            Date tanggalKembali =
                    sdf.parse(jTextField3.getText());

            long selisih =
                    tanggalKembali.getTime()
                    - jatuhTempo.getTime();

            int terlambat =
                    (int)(selisih / (1000 * 60 * 60 * 24));

            if (terlambat < 0) {

                terlambat = 0;

            }

            int denda = terlambat * 50000;

            jTextField4.setText(String.valueOf(terlambat));

            jTextField5.setText(String.valueOf(denda));

        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null,
                e.getMessage());

        }
    }//GEN-LAST:event_jButton1ActionPerformed

