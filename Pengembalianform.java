    private void tampilComboTransaksi() {

    try {

        String sql = "SELECT id_transaksi FROM transaksi";

        Connection conn = koneksi.koneksi.getKoneksi();
        Statement stm = conn.createStatement();
        ResultSet rs = stm.executeQuery(sql);

        jComboBox1.removeAllItems();

        while (rs.next()) {

            jComboBox1.addItem(rs.getString("id_transaksi"));

        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e.getMessage());

    }

}
    
    private void bersih() {

    jTextField1.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    jTextField5.setText("");

    if (jComboBox1.getItemCount() > 0) {
        jComboBox1.setSelectedIndex(0);
    }

}
    
    private void simpan() {

    try {

        String sql =
                "INSERT INTO pengembalian VALUES (?,?,?,?,?)";

        PreparedStatement pst =
                koneksi.koneksi.getKoneksi().prepareStatement(sql);

        pst.setString(1, jTextField1.getText());

        pst.setString(2,
                jComboBox1.getSelectedItem().toString());

        pst.setString(3, jTextField3.getText());

        pst.setInt(4,
                Integer.parseInt(jTextField4.getText()));

        pst.setInt(5,
                Integer.parseInt(jTextField5.getText()));

        pst.executeUpdate();

        JOptionPane.showMessageDialog(null,
                "Data berhasil disimpan");

        bersih();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null,
                e.getMessage());

    }

}
